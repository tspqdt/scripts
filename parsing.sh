#!/bin/bash
if ["$1" == ""]
then
    echo "##############################################################################################"
    echo "->                           DESEC - PARSING SCAN                        <-             "
    echo "->                       Exemplo: $0 www.alvo.com.br                     <-             "
    echo "##############################################################################################"
else
    echo "##############################################################################################"
    echo "->                               Buscando Host                                            "
    echo "##############################################################################################"
wget $1
grep href index.html | cut -d "/" -f3 | grep "\." | cut -d '"' -f 1 | grep -v "<l" > lista;

    echo "##############################################################################################"
    echo "->                               Resolvendo Host                                            "
    echo "##############################################################################################"
for url in $(cat lista);do host $url | grep "has address";
done
fi